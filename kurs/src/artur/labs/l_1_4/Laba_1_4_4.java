public class Laba_1_4_4 {
    public static void main(String[] args) {
        int i=0;
        System.out.println("Increment in postfix mode: " + i++);
        System.out.println("Increment in prefix mode: " + ++i );
        System.out.println("Decrement in postfix mode: " + i--);
        System.out.println("Decrement in postfix mode: " + --i);
    }
}
