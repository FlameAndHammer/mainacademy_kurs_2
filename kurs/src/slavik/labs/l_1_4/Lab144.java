package slavik.labs.l_1_4;
public class Lab144 {
    public static void main(String[] args) {
        int a = 1;
        for (int i = 0; i < a; i++) {
            System.out.println("inc i++ =" + i++);
            System.out.println("dec i-- =" + i--);
            System.out.println("inc ++i =" + ++i);
            System.out.println("dec --i =" + --i);
        }
    }
}